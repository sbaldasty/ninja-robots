package org.bitflippin.ninjarobots;

// Exceptions that kill a robot at runtime.
// These will be avoided with good programs.
// The message is dumped into the death area.

public class ExecutionException extends Exception  {

	// Error message that should be dumped.
	public String getError()  { return error; }
	private String error;

	// Initialize variables.
	public ExecutionException(String s)  {
		super();
		error = s;
	}

}
